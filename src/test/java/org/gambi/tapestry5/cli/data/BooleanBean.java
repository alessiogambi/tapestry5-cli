package org.gambi.tapestry5.cli.data;

public class BooleanBean {

	boolean jonny;

	boolean tommy;

	public BooleanBean() {
	}

	public void setTommy(boolean tommy) {
		this.tommy = tommy;
	}

	public boolean isTommy() {
		return tommy;
	}

	public void setJonny(boolean jonny) {
		this.jonny = jonny;
	}

	public boolean isJonny() {
		return jonny;
	}
}
